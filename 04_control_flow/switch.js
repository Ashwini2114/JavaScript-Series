//REMEMBER :---- If break nahi likha hai toh condition match krne ke baad wala sabhi case execute ho jaega default ke alawa
const month = 3
switch (month) {
    case 1:
        console.log("January");
        break;
    case 2:
        console.log("February");
        break;
    case 3:
        console.log("March");
        break;
    case 4:
        console.log("April");
        break;
    case 5:
        console.log("May");
        break;

    default:
        console.log("Choose between 1 and 5");
        break;
}











